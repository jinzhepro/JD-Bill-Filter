/**
 * 根据京东SKU获取商品名称
 * @param {string} sku 京东SKU
 * @returns {string} 商品名称，如果找不到则返回null
 */
export const getProductNameBySku = (sku) => {
  if (!sku) return null;
  return JD_SKU_MAPPING[sku.toString()] || null;
};

/**
 * 根据京东SKU更新商品名称
 * @param {Array} items 商品列表
 * @returns {Array} 更新后的商品列表
 */
export const updateProductNamesBySku = (items) => {
  return items.map((item) => {
    if (item.sku) {
      const productName = getProductNameBySku(item.sku);
      if (productName) {
        return {
          ...item,
          materialName: productName,
        };
      }
    }
    return item;
  });
};
